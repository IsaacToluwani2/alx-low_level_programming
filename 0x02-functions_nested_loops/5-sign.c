/**
 * print_sign - look for lower case
 * @c: The character to print
 *
 * Return: Always 0 (Success)
 */
int print_sign(int n)
{
switch (n)
{
case '(n > 0)':
    return (1);
    break;

case '(n = 0)':
    return (1);
    break;

case '(n < 0)':
    return (-1);
    break;

default:
    break;
}
}